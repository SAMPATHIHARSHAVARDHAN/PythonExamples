#Sample1(Strings)
fruits=["Apples","Grapes","FineApple","GreenApple","Banana"]
print(fruits)
print(fruits[0])
print(fruits[1:2])
print(fruits[:2])
print(fruits[0:])

result="Apples" not in fruits
print(result)
#using For Loop
for x in fruits:
    print(x)

#Sample2(Other than Strings)
